def generate_password(n):
    result = ""
    pairs = []
    # Генерируем уникальные пары чисел от 1 до 20
    for i in range(1, 21):
        for j in range(i + 1, 21):
            # Проверяем кратность
            pair_sum = i + j
            if n % pair_sum == 0:
                pairs.append(f"{i}{j}")

    result = ''.join(pairs)
    return result

